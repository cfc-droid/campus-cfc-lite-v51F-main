/* ===== VALIDADOR CONTRACTUAL ===== */

export function validateQuestionsCount(){

if(!Array.isArray(QUESTIONS_V1)){
throw new Error("[PIF] QUESTIONS_V1 no es array");
}

if(QUESTIONS_V1.length !== 30){
throw new Error(`[PIF] Deben existir 30 preguntas. Actual: ${QUESTIONS_V1.length}`);
}

QUESTIONS_V1.forEach(q=>{
if(!q.id || !q.text || !q.options){
throw new Error(`[PIF] Pregunta mal formada: ${q.id}`);
}

if(q.options.length !== 5){
throw new Error(`[PIF] Pregunta ${q.id} debe tener 5 opciones`);
}
});

return true;
}

